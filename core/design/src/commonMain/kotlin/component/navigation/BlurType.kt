package component.navigation

/**
 * Перечисление типов размытия, которые могут быть применены к навигационным компонентам.
 */
enum class BlurType {
    /**
     * Без размытия - обычный фон.
     */
    NONE,

    /**
     * Эффект стекла - прозрачный с минимальным шумом.
     */
    GLASS,

    /**
     * Эффект матового стекла - с легким шумом и более сильным размытием.
     */
    FROSTED,

    /**
     * Эффект акрила - с более сильным размытием и шумом.
     * Имитирует эффект акрилового материала из Windows.
     */
    ACRYLIC,

    /**
     * Эффект слюды - с максимальным размытием и шумом.
     * Имитирует эффект материала Mica из Windows 11.
     */
    MICA
}
