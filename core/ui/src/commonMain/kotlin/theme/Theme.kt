internal val LocalThemeIsDark = compositionLocalOf { mutableStateOf(true) }


@Composable
internal fun AppTheme(
    theme: Int,
    content: @Composable() () -> Unit
) {
    val systemIsDark = isSystemInDarkTheme()
    val isDarkState = remember { mutableStateOf(systemIsDark) }

    // Подписываемся на изменения StateFlow
    when (theme) {
        MultiplatformSettings.ThemeOption.THEME_SYSTEM -> isDarkState.value = systemIsDark
        MultiplatformSettings.ThemeOption.THEME_DARK -> isDarkState.value = true
        MultiplatformSettings.ThemeOption.THEME_LIGHT -> isDarkState.value = false
    }

    val isDark by isDarkState
    val colorScheme = (if (isDark) darkColorScheme else lightColorScheme).switch()
//    // Gradient colors
    val emptyGradientColors = GradientColors(container = colorScheme.surfaceColorAtElevation(2.dp))
    val defaultGradientColors = GradientColors(
        top = colorScheme.inverseOnSurface,
        bottom = colorScheme.primaryContainer,
        container = colorScheme.surface,
    )
    val gradientColors = if (isDark) DarkAndroidGradientColors else LightAndroidGradientColors

    // Background theme
    val defaultBackgroundTheme = BackgroundTheme(
        color = colorScheme.surface,
        tonalElevation = 2.dp,
    )
    val backgroundTheme =
        if (isDark) DarkAndroidBackgroundTheme else LightAndroidBackgroundTheme

    val tintTheme = TintTheme(colorScheme.primary)


    // Composition locals
    CompositionLocalProvider(
        LocalThemeIsDark provides isDarkState,
        LocalGradientColors provides gradientColors,
        LocalBackgroundTheme provides backgroundTheme,
        LocalTintTheme provides tintTheme,
    ) {
        SystemAppearance(!isDark)
        MaterialTheme(
            colorScheme = colorScheme,
            typography = AppTypography,
            shapes = AppShapes,
            content = content
        )
    }
}


