#!/bin/bash

# Остановка и удаление контейнеров
echo "Остановка и удаление контейнеров..."

if [ "$1" == "--volumes" ] || [ "$1" == "-v" ]; then
    echo "Удаление контейнеров вместе с томами..."
    docker-compose down -v
else
    echo "Удаление контейнеров (тома сохраняются)..."
    docker-compose down
fi

echo "Готово!"
